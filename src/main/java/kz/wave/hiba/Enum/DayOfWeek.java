package kz.wave.hiba.Enum;

/**
 * The enum represents the days of the week.
 */
public enum DayOfWeek {
    /**
     * Represents Monday.
     */
    MONDAY,

    /**
     * Represents Tuesday.
     */
    TUESDAY,

    /**
     * Represents Wednesday.
     */
    WEDNESDAY,

    /**
     * Represents Thursday.
     */
    THURSDAY,

    /**
     * Represents Friday.
     */
    FRIDAY,

    /**
     * Represents Saturday.
     */
    SATURDAY,

    /**
     * Represents Sunday.
     */
    SUNDAY
}
