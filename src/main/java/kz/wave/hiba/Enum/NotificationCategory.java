package kz.wave.hiba.Enum;

/**
 * The enum represents different categories of notifications.
 */
public enum NotificationCategory {
    /**
     * System alerts.
     */
    SYSTEM_ALERTS,

    /**
     * User messages.
     */
    USER_MESSAGES,

    /**
     * Reminders.
     */
    REMINDERS,

    /**
     * Promotions and offers.
     */
    PROMOTIONS,

    /**
     * Warnings.
     */
    WARNINGS,

    /**
     * News.
     */
    NEWS,

    /**
     * Updates.
     */
    UPDATES,

    /**
     * Orders.
     */
    ORDERS
}
