package kz.wave.hiba.Enum;

/**
 * The enum represents the type of sender in a chat message.
 */
public enum SenderType {
    /**
     * The sender is a client.
     */
    CLIENT,

    /**
     * The sender is a support representative.
     */
    SUPPORT,

    /**
     * The sender is a bot.
     */
    BOT
}
