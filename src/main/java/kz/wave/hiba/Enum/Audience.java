package kz.wave.hiba.Enum;

/**
 * The enum represents the audience for a promotion or notification.
 */
public enum Audience {
    /**
     * Audience includes everyone.
     */
    ALL,

    /**
     * Audience includes only butcheries.
     */
    BUTCHERIES,

    /**
     * Audience includes only clients.
     */
    CLIENTS
}
