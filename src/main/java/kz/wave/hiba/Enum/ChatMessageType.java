package kz.wave.hiba.Enum;

/**
 * The enum represents the types of chat messages.
 */
public enum ChatMessageType {
    /**
     * Represents the start of a dialog.
     */
    START_DIALOG,

    /**
     * Represents the end of a dialog.
     */
    END_DIALOG,

    /**
     * Represents a standard message.
     */
    MESSAGE;
}
