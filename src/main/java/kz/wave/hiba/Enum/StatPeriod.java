package kz.wave.hiba.Enum;

/**
 * The enum represents different periods for statistical analysis.
 */
public enum StatPeriod {
    /**
     * Statistics for today.
     */
    TODAY,

    /**
     * Statistics for the current week.
     */
    WEEK,

    /**
     * Statistics for the current quarter.
     */
    QUARTER,

    /**
     * Statistics for the current month.
     */
    MONTH,

    /**
     * Statistics for the past half year.
     */
    HALFYEAR,

    /**
     * Statistics for the current year.
     */
    YEAR,

    /**
     * Statistics for all available time.
     */
    ALL
}
