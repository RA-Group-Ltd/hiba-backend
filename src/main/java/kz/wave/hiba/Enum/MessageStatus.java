package kz.wave.hiba.Enum;

/**
 * The enum represents the status of a message.
 */
public enum MessageStatus {
    /**
     * The message has been received by the recipient.
     */
    RECEIVED,

    /**
     * The message has been delivered to the recipient.
     */
    DELIVERED,

    /**
     * The message has been sent by the sender.
     */
    SEND
}
